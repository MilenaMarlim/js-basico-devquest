function soma(numero1, numero2) {
    return numero1 + numero2
}

let resultadoFinalSoma = soma(8, 7);
console.log('A soma dos dois números é: ' + resultadoFinalSoma)


function subtracao(numero1, numero2) {
    return numero1 - numero2
}

let resultadoFinalSubtracao = subtracao(10, 1)
console.log('A subtracão dos dois números é: ' + resultadoFinalSubtracao)


function multiplicacao(numero1, numero2) {
    return numero1 * numero2
}

let resultadoFinalMultiplicacao = multiplicacao(4, 2)

console.log('O resultado da multiplicação entre os dois números é: ' + resultadoFinalMultiplicacao)


function divisao(numero1, numero2) {
    return numero1 / numero2
}

let resultadoFinalDivisao = divisao(4, 2)
console.log('O resultado da divisão entre os dois números é: ' + resultadoFinalDivisao)


